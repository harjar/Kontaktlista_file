    private void btnLoadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoadActionPerformed
        ArrayList<Kontakt> kon = filemgr.readFromFile();
        konmgr.createNewList(kon);
        if (this.rbtFörnamn.isSelected()) {
            this.skrivUtFirstName();
        } else {
            this.skrivUtLastName();
        }
    }//GEN-LAST:event_btnLoadActionPerformed

    private void skrivUtFirstName() {
        this.txaKontaktArea.setText("");
        ArrayList<Kontakt> lista = konmgr.getLista();
        for (int i = 0; i < lista.size(); i++) {
            Kontakt kon = lista.get(i);
            this.txaKontaktArea.append(kon.getFörnamn() + "\t"
                    + kon.getEfternamn() + "\t"
                    + kon.getTelefon() + "\n");
        }
    }

    private void skrivUtLastName() {
        ArrayList<Kontakt> lista = konmgr.getLista();
        this.txaKontaktArea.setText("");
        for (int i = 0; i < lista.size(); i++) {
            Kontakt kon = lista.get(i);
            this.txaKontaktArea.append(kon.getEfternamn() + "\t"
                    + kon.getFörnamn() + "\t"
                    + kon.getTelefon() + "\n");
        }
    }

