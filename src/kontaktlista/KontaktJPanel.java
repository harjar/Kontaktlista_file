    private void btnAddKontaktActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddKontaktActionPerformed
        String fNamn = this.txfFörnamn.getText();
        String eNamn = this.txfEfternamn.getText();
        String tele = this.txfTele.getText();

        this.lblError.setText("");

        if (!fNamn.trim().isEmpty()
                && !eNamn.trim().isEmpty()
                && !tele.trim().isEmpty()) {
            this.lblError.setVisible(false);
            this.txfEfternamn.setText("");
            this.txfFörnamn.setText("");
            this.txfTele.setText("");
            Kontakt kon = new Kontakt(fNamn, eNamn, tele);
            konmgr.addKontakt(kon);

            if (this.rbtFörnamn.isSelected()) {
                this.skrivUtFirstName();
            } else {
                this.skrivUtLastName();
            }
        } else {
            this.lblError.setText("Fyll i alla fält.");
            this.lblError.setVisible(true);
        }
    }//GEN-LAST:event_btnAddKontaktActionPerformed

